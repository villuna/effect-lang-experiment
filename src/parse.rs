//! This module defines the abstract syntax tree (AST) of the language and provides a function to
//! parse the AST from the source code ([parse]).

// Most of the functions in this module are of the form parse_RULE which simply transforms the
// concrete syntax tree given to us by Pest's parser and turns it into the abstract syntax tree.

use std::{collections::HashMap, sync::LazyLock};

use pest::{
    Parser,
    iterators::{Pair, Pairs},
    pratt_parser::{Assoc, Op, PrattParser},
};
use pest_derive::Parser;

pub type Identifier = String;

#[derive(Debug, Clone)]
pub struct ProgramTree {
    pub functions: HashMap<Identifier, FunctionDefinition>,
}

#[derive(Debug, Clone)]
pub struct FunctionDefinition {
    pub name: Identifier,
    pub params: Vec<FunctionParam>,
    pub block: Block,
    pub return_type: Option<Type>,
}

#[derive(Debug, Clone)]
pub struct FunctionParam {
    pub name: Identifier,
    pub ty: Type,
}

#[derive(Debug, Clone)]
pub struct Block {
    pub statements: Vec<Statement>,
    // This has to be boxed since Expression could be a Block
    pub value: Option<Box<Expression>>,
}

#[derive(Debug, Clone)]
pub enum Value {
    Unit,
    Int(i64),
    Float(f64),
    String(String),
    Bool(bool),
}

#[derive(Debug, Clone, Copy)]
pub enum BinOp {
    Add,
    Sub,
    Mult,
    Div,
    Eq,
    Neq,
    Gt,
    Geq,
    Lt,
    Leq,
    And,
    Or,
}

#[derive(Copy, Clone, Debug)]
pub enum UnaryOp {
    Not,
    Neg,
}
impl UnaryOp {
    fn from_rule(rule: Rule) -> Option<UnaryOp> {
        let op = match rule {
            Rule::neg => UnaryOp::Neg,
            Rule::not => UnaryOp::Not,
            _ => return None,
        };
        Some(op)
    }
}

impl BinOp {
    fn from_rule(rule: Rule) -> Option<Self> {
        let op = match rule {
            Rule::add => BinOp::Add,
            Rule::sub => BinOp::Sub,
            Rule::mult => BinOp::Mult,
            Rule::div => BinOp::Div,
            Rule::eq => BinOp::Eq,
            Rule::neq => BinOp::Neq,
            Rule::gt => BinOp::Gt,
            Rule::geq => BinOp::Geq,
            Rule::lt => BinOp::Lt,
            Rule::leq => BinOp::Leq,
            Rule::and => BinOp::And,
            Rule::or => BinOp::Or,
            _ => return None,
        };

        Some(op)
    }
}

#[derive(Debug, Clone)]
pub enum Expression {
    Value(Value),
    Block(Block),
    Var(Identifier),
    FunctionCall {
        function: Identifier,
        parameters: Vec<Expression>,
    },
    BinOp(Box<Expression>, BinOp, Box<Expression>),
    UnaryOp(UnaryOp, Box<Expression>),
    Conditional {
        condition: Box<Expression>,
        if_path: Box<Expression>,
        else_path: Option<Box<Expression>>,
    },
}

#[derive(Debug, Clone)]
pub enum Statement {
    VariableDefinition {
        name: Identifier,
        ty: Option<Type>,
        value: Expression,
    },
    Expression(Expression),
}

#[derive(Debug, Clone)]
pub enum Type {
    Primitive(PrimitiveType),
}

#[derive(Debug, Clone, Copy)]
pub enum PrimitiveType {
    Int,
    Float,
    String,
    Bool,
}

type ParseResult<T> = Result<T, pest::error::Error<Rule>>;

/// This struct holds code auto-generated by pest to parse text to a (non-abstract) syntax tree.
#[derive(Parser)]
#[grammar = "grammar.pest"]
struct LangParser;

/// A Pratt parser allows us to parse binary operations with precedence defined here, outside of
/// the grammar.
///
/// The precedence order is defined by a sequence of calls to .op(...), with lowest precedence
/// first.
static PRATT_PARSER: LazyLock<PrattParser<Rule>> = LazyLock::new(|| {
    PrattParser::new()
        .op(Op::infix(Rule::eq, Assoc::Left) | Op::infix(Rule::neq, Assoc::Left))
        .op(Op::infix(Rule::and, Assoc::Left) | Op::infix(Rule::or, Assoc::Left))
        .op(Op::infix(Rule::gt, Assoc::Left)
            | Op::infix(Rule::geq, Assoc::Left)
            | Op::infix(Rule::lt, Assoc::Left)
            | Op::infix(Rule::leq, Assoc::Left))
        .op(Op::infix(Rule::add, Assoc::Left) | Op::infix(Rule::sub, Assoc::Left))
        .op(Op::infix(Rule::mult, Assoc::Left) | Op::infix(Rule::div, Assoc::Left))
        .op(Op::prefix(Rule::not) | Op::prefix(Rule::neg))
});

/// Parses the entire text of a program file and converts it into an abstract syntax tree
/// representing the declarations in the file.
pub fn parse(source: &str) -> ParseResult<ProgramTree> {
    let parse = LangParser::parse(Rule::program, source)?.next().unwrap();

    let functions = parse
        .into_inner()
        .filter_map(|parsed| match parsed.as_rule() {
            Rule::function_def => {
                Some(parse_function_def(parsed.into_inner()).map(|f| (f.name.clone(), f)))
            }
            Rule::EOI => None,
            _ => unreachable!(),
        })
        .collect::<ParseResult<_>>()?;

    Ok(ProgramTree { functions })
}

// function_def = { "fun" ~ ident ~ "(" ~ params_list? ~ ")" ~ ("->" ~ type)? ~ block }
fn parse_function_def(mut input: Pairs<'_, Rule>) -> ParseResult<FunctionDefinition> {
    let name = input.next().unwrap();

    let name = match name.as_rule() {
        Rule::ident => name.to_string(),
        _ => unreachable!(),
    };

    let mut params = Vec::new();
    let mut return_type = None;
    let mut block = None;

    for pair in input {
        match pair.as_rule() {
            Rule::params_list => params = parse_params_list(pair.into_inner())?,
            Rule::r#type => return_type = Some(parse_type(pair)?),
            Rule::block => block = Some(parse_block(pair.into_inner())?),
            _ => unreachable!(),
        }
    }

    Ok(FunctionDefinition {
        name,
        params,
        block: block.unwrap(),
        return_type,
    })
}

fn parse_params_list(input: Pairs<'_, Rule>) -> ParseResult<Vec<FunctionParam>> {
    input
        .map(|param| {
            assert_eq!(param.as_rule(), Rule::param);
            parse_param(param.into_inner())
        })
        .collect()
}

// param = { ident ~ ":" ~ type }
fn parse_param(mut input: Pairs<'_, Rule>) -> ParseResult<FunctionParam> {
    let name = input.next().unwrap();
    assert_eq!(name.as_rule(), Rule::ident);
    let ty = input.next().unwrap();

    Ok(FunctionParam {
        name: name.to_string(),
        ty: parse_type(ty)?,
    })
}

fn parse_block(mut input: Pairs<'_, Rule>) -> ParseResult<Block> {
    let statements = input.next().unwrap().into_inner();
    let mut result = Block {
        statements: vec![],
        value: None,
    };

    for pair in statements {
        match pair.as_rule() {
            Rule::statement => {
                result.statements.push(parse_statement(pair.into_inner())?);
            }
            Rule::expression => {
                result.value = Some(Box::new(parse_expression(pair.into_inner())?));
            }
            _ => panic!(
                "encountered unexpected rule \"{:?}\"\ninput: \"{}\"",
                pair.as_rule(),
                pair.as_str()
            ),
        }
    }

    Ok(result)
}

fn parse_statement(mut input: Pairs<'_, Rule>) -> ParseResult<Statement> {
    let rule = input.next().unwrap();

    match rule.as_rule() {
        Rule::variable_def => {
            // variable_def = { "let" ~ ident ~ (":" ~ type)? ~ "=" ~ expression }
            let mut input = rule.into_inner();
            let name = input.next().unwrap();
            let mut ty = None;
            let mut value = None;

            for pair in input {
                match pair.as_rule() {
                    Rule::r#type => ty = Some(parse_type(pair)?),
                    Rule::expression => value = Some(parse_expression(pair.into_inner())?),
                    _ => unreachable!(),
                }
            }

            Ok(Statement::VariableDefinition {
                name: name.to_string(),
                ty,
                value: value.unwrap(),
            })
        }
        Rule::expression => Ok(Statement::Expression(parse_expression(rule.into_inner())?)),
        _ => unreachable!(),
    }
}

fn parse_expression(input: Pairs<'_, Rule>) -> ParseResult<Expression> {
    PRATT_PARSER
        .map_primary(|pair| parse_primary(pair.into_inner()))
        .map_infix(|lhs, op, rhs| {
            let lhs = lhs?;
            let rhs = rhs?;
            let Some(op) = BinOp::from_rule(op.as_rule()) else {
                unreachable!()
            };

            Ok(Expression::BinOp(Box::new(lhs), op, Box::new(rhs)))
        })
        .map_prefix(|op, expr| {
            let expr = expr?;
            let Some(op) = UnaryOp::from_rule(op.as_rule()) else {
                unreachable!()
            };
            Ok(Expression::UnaryOp(op, Box::new(expr)))
        })
        .parse(input)
}

fn parse_primary(mut input: Pairs<'_, Rule>) -> ParseResult<Expression> {
    let input = input.next().unwrap();
    Ok(match input.as_rule() {
        Rule::unit => Expression::Value(Value::Unit),
        Rule::number => {
            let mut inner_input = input.clone().into_inner();
            let int_part = inner_input.next().unwrap();
            let decimal_part = inner_input.next();
            let exponential_part = inner_input.next();

            if matches!((decimal_part, exponential_part), (None, None)) {
                assert!(matches!(int_part.as_rule(), Rule::int));
                Expression::Value(Value::Int(int_part.as_str().parse().unwrap()))
            } else {
                Expression::Value(Value::Float(input.as_str().parse().unwrap()))
            }
        }
        Rule::string => {
            let input = input.into_inner().next().unwrap();

            // TODO handle escape chars
            Expression::Value(Value::String(input.to_string()))
        }
        Rule::function_call => {
            let mut input = input.into_inner();
            let name = input.next().unwrap();
            let mut parameters = Vec::new();

            for expr in input {
                match expr.as_rule() {
                    Rule::expression => parameters.push(parse_expression(expr.into_inner())?),
                    _ => unreachable!(),
                }
            }

            Expression::FunctionCall {
                function: name.to_string(),
                parameters,
            }
        }
        Rule::ident => Expression::Var(input.to_string()),
        Rule::block => Expression::Block(parse_block(input.into_inner())?),
        Rule::bool => Expression::Value(Value::Bool(input.as_str() == "true")),
        Rule::conditional => {
            let mut input = input.into_inner();
            let condition = input.next().unwrap();
            assert_eq!(condition.as_rule(), Rule::expression);
            let condition = parse_expression(condition.into_inner())?;

            let if_path = input.next().unwrap();
            assert_eq!(if_path.as_rule(), Rule::block);
            let if_path = Expression::Block(parse_block(if_path.into_inner())?);

            // We need to clone this as if the next part of the if statement is another conditional,
            // we have to parse the entire conditional.
            let else_path: Option<Expression> = input
                .clone()
                .next()
                .map(|else_path| match else_path.as_rule() {
                    Rule::block => Ok(Expression::Block(parse_block(else_path.into_inner())?)),
                    Rule::conditional => parse_primary(input),
                    _ => unreachable!(),
                })
                .transpose()?;

            Expression::Conditional {
                condition: Box::new(condition),
                if_path: Box::new(if_path),
                else_path: else_path.map(Box::new),
            }
        }
        Rule::expression => parse_expression(input.into_inner())?,
        _ => {
            dbg!(input.as_rule());
            unreachable!()
        }
    })
}

// type = { primitive_type }
fn parse_type(input: Pair<'_, Rule>) -> ParseResult<Type> {
    Ok(Type::Primitive(parse_primitive_type(
        input.into_inner().next().unwrap(),
    )?))
}

// primitive_type = { "string" | "int" | "float" | "bool" }
fn parse_primitive_type(input: Pair<'_, Rule>) -> ParseResult<PrimitiveType> {
    Ok(match input.as_str() {
        "string" => PrimitiveType::String,
        "int" => PrimitiveType::Int,
        "float" => PrimitiveType::Float,
        "bool" => PrimitiveType::Bool,
        _ => unreachable!(),
    })
}
